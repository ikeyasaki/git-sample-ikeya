package com.example.demo;

public class Car {
	/*
	 * ここから平山が編集
	 */
	private String name;
	private Integer speed;

	public Car() {

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getSpeed() {
		return speed;
	}

	public void setSpeed(Integer speed) {
		this.speed = speed;
	}
	/*
	 * ここまで編集
	 */
}
